    import Parallax from '../../libs/parallaxie'
    import Isotope from '../../libs/isotope.pkgd.min'


    export default {
        name: 'dev',
        components: {
            Order,
            Footer,
            Modal,
            BackToTop,
            Parallax,
            Isotope
        },

        mounted() {
            // initialize parallaxie
            $('.parallaxie').parallaxie({speed: 0.5,});

            // initialize isotope
            let $container = $('.portfolio_container');
            $container.isotope({
                filter: '*',
            });

            $('.portfolio_filter a').click(function () {
                $('.portfolio_filter .active').removeClass('active');
                $(this).addClass('active');

                let selector = $(this).attr('data-filter');
                $container.isotope({
                    filter: selector,
                    animationOptions: {
                        duration: 500,
                        animationEngine: "jquery"
                    }
                });
                return false;
            });

        },

        methods: {
            showModal() {
                document.querySelector('.modal-wrap').classList.remove('hide');
            },
            closeModal() {
                document.querySelectorAll('.modal-wrap').forEach(function (element) {
                    element.classList.add('hide');
                });
            },
        },
    };
</script>
